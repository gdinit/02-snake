#!/usr/local/bin/dash

########################################################################################################################
# l1a.wipe_build_dir_then_run_cmake_build_unix_makefiles.sh
########################################################################################################################


# MAIN #################################################################################################################
/bin/echo			""					&& \
/bin/echo			"removing build dir..."			&& \
/bin/rm -rf			"../build"				&& \
/bin/echo			""					&& \
/bin/echo			"recreating build dir..."		&& \
/bin/mkdir			"../build"				&& \
/bin/echo			""					&& \
cd				"../build"				&& \
/usr/local/bin/cmake -G		"Unix Makefiles"			"../source"
# EOF ##################################################################################################################

