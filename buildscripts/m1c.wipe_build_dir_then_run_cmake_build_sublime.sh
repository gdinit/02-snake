#!/usr/local/bin/dash

########################################################################################################################
# m1c.wipe_build_dir_then_run_cmake_build_sublime.sh
########################################################################################################################


# MAIN #################################################################################################################
/bin/echo			""					&& \
/bin/echo			"removing build dir..."			&& \
/bin/rm -rf			"../build"				&& \
/bin/echo			""					&& \
/bin/echo			"recreating build dir..."		&& \
/bin/mkdir			"../build"				&& \
/bin/echo			""					&& \
cd				"../build"				&& \
/usr/local/bin/cmake -G		"Sublime Text 2 - Unix Makefiles"	../source
# EOF ##################################################################################################################

